def get_multilingual_training_data(language=None):
    """
    Get training data for a specific language or all languages.
    
    Args:
        language (str): Language code (e.g., 'hi-IN', 'ta-IN') or None for all languages
    
    Returns:
        list: List of training examples (text, ipc_sections)
    """
    if language and language in MULTILINGUAL_TRAINING_DATA:
        return MULTILINGUAL_TRAINING_DATA[language]
    
    # Return all training data if no specific language requested
    all_data = []
    for lang_data in MULTILINGUAL_TRAINING_DATA.values():
        all_data.extend(lang_data)
    
    return all_data

def get_language_keywords(language):
    """
    Get IPC keywords for a specific language.
    
    Args:
        language (str): Language code (e.g., 'hi-IN', 'ta-IN')
    
    Returns:
        dict: Dictionary of IPC section keywords for the language
    """
    return MULTILINGUAL_IPC_KEYWORDS.get(language, {})

def get_supported_languages():
    """
    Get list of languages with multilingual training data.
    
    Returns:
        list: List of supported language codes
    """
    return list(MULTILINGUAL_TRAINING_DATA.keys()) 